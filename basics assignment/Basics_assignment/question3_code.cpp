// Q3. How much space does the following data types take?
// a. int
// b. bool  
// c. float
#include<iostream>
using namespace std;

int main(){
     int a;
     cout<<sizeof(a)<<endl;

     bool b;
     cout<<sizeof(b)<<endl;

     float c;
     cout<<sizeof(c)<<endl;
     return 0;
}